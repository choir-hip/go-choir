#!/usr/bin/env node
import { createHash } from 'node:crypto';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '../..');
const schemaPath = resolve(root, 'internal/sourcecontract/source_contract_schema.json');
const outputPath = resolve(root, 'frontend/src/lib/source-contract.generated.ts');
const check = process.argv.includes('--check');

const raw = readFileSync(schemaPath, 'utf8');
const schema = JSON.parse(raw);
const hash = createHash('sha256').update(raw).digest('hex');

const camelCase = (value) => value.replace(/_([a-z])/g, (_, letter) => letter.toUpperCase());
const valuesObject = (entries, overrides = {}) => Object.fromEntries(
  Object.keys(entries).map((key) => [overrides[key] || camelCase(key), key]),
);

const generated = `// Generated by frontend/scripts/generate-source-contract.mjs. Do not edit by hand.
// source-contract-schema-sha256: ${hash}

export const SOURCE_CONTRACT_SCHEMA = ${JSON.stringify(schema, null, 2)} as const;

export const SOURCE_EVIDENCE_STATES = ${JSON.stringify(valuesObject(schema.evidence_states), null, 2)} as const;

export const SOURCE_OPEN_SURFACES = ${JSON.stringify(valuesObject(schema.open_surfaces), null, 2)} as const;

export const READER_ARTIFACT_STATES = ${JSON.stringify(valuesObject(schema.reader_artifact_states, {
  reader_snapshot_ready: 'ready',
}), null, 2)} as const;

export const SOURCE_SELECTOR_KINDS = ${JSON.stringify(valuesObject(schema.selector_kinds), null, 2)} as const;
`;

if (check) {
  const current = readFileSync(outputPath, 'utf8');
  if (current !== generated) {
    console.error('frontend/src/lib/source-contract.generated.ts is stale; run frontend/scripts/generate-source-contract.mjs');
    process.exit(1);
  }
  process.exit(0);
}

writeFileSync(outputPath, generated);
